export const i18n = {
  detectLanguage: (): SupportedLanguage => {
    const lang = navigator.language.split('-')[0];
    const supported: SupportedLanguage[] = ['sr', 'en', 'de', 'fr', 'es'];
    return supported.includes(lang as SupportedLanguage) ? (lang as SupportedLanguage) : 'en';
  },
  t: (key: string, lang: SupportedLanguage = 'en'): string => {
    return DICTIONARY[lang]?.[key] || DICTIONARY['en']?.[key] || key;
  },
  getLanguageName: (lang: SupportedLanguage): string => {
    return DICTIONARY[lang]?.languageDisplay || lang;
  }
};
